package sampleprogram;

public class Looping {
	public static void main(String[]args) {
		
		int i = 0;
		
		while(i<=10) {
			System.out.println(i);
			i++;
			
		}
		
		
		
		int a = 12;
		
		do {
			System.out.println(a);
		}while(a<=10);
		a++;
	
	
	for(int b=0; b<=10; b++) {
		System.out.println(b);
	}

	}
}
